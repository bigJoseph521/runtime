@dataclass(frozen=True, slots=True, kw_only=True)
class TradingHaltedEvent(MarketEvent):
    """
    Represent a temporary trading halt.

    Attributes
    ----------
    scope
        Scope affected by the trading halt.
    symbol
        Security affected by a symbol-level halt.

        This field is required when ``scope`` is
        ``TradingHaltScope.SYMBOL``.
    market
        Market affected by a market-wide halt.

        This field is required when ``scope`` is
        ``TradingHaltScope.MARKET``.
    venue
        Exchange or trading venue affected by a venue-wide halt.

        This field is required when ``scope`` is
        ``TradingHaltScope.VENUE``.
    reason
        Platform- or exchange-provided explanation for the halt.
    halt_code
        Platform- or exchange-provided halt reason code.
    expected_resume_at
        Expected trading-resumption time.

        ``None`` indicates that no expected resumption time is available.
    """

    type: MarketEventType = field(
        default=MarketEventType.TRADING_HALTED,
        init=False,
    )

    scope: TradingHaltScope
    symbol: Symbol | None = None
    market: str | None = None
    venue: str | None = None
    reason: str | None = None
    halt_code: str | None = None
    expected_resume_at: Timestamp | None = None

    def __post_init__(self) -> None:
        """
        Validate that the event identifies its affected scope.
        """
        if self.scope is TradingHaltScope.SYMBOL and self.symbol is None:
            raise ValueError(
                "symbol is required when scope is TradingHaltScope.SYMBOL"
            )

        if self.scope is TradingHaltScope.MARKET and self.market is None:
            raise ValueError(
                "market is required when scope is TradingHaltScope.MARKET"
            )

        if self.scope is TradingHaltScope.VENUE and self.venue is None:
            raise ValueError(
                "venue is required when scope is TradingHaltScope.VENUE"
            )


