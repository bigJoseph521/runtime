class MarketEventType(StrEnum):
    """
    Define market-related event types generated by the platform.

    Market events are delivered to strategies through
    ``Strategy.on_event()``.

    Members
    -------
    PRE_MARKET_OPEN
        Indicates that the pre-market trading session has opened.
    MARKET_OPEN
        Indicates that the regular trading session has opened.
    MARKET_CLOSE
        Indicates that the regular trading session has closed.
    POST_MARKET_CLOSE
        Indicates that the post-market trading session has closed.
    TRADING_HALTED
        Indicates that trading has been temporarily halted.
    TRADING_RESUMED
        Indicates that trading has resumed after a halt.
    SPLIT
        Indicates a stock split or reverse stock split.
    DIVIDEND
        Indicates a cash-dividend distribution.
    SYMBOL_CHANGE
        Indicates that a security's trading symbol has changed.
    DELISTING_WARNING
        Indicates that a security is scheduled for delisting.
    DELISTING
        Indicates that a security has been delisted.
    """

    PRE_MARKET_OPEN = "pre_market_open"
    MARKET_OPEN = "market_open"
    MARKET_CLOSE = "market_close"
    POST_MARKET_CLOSE = "post_market_close"

    TRADING_HALTED = "trading_halted"
    TRADING_RESUMED = "trading_resumed"

    SPLIT = "split"
    DIVIDEND = "dividend"
    SYMBOL_CHANGE = "symbol_change"
    DELISTING_WARNING = "delisting_warning"
    DELISTING = "delisting"


class TradingHaltScope(StrEnum):
    """
    Define the scope affected by a trading halt or resumption.

    Members
    -------
    SYMBOL
        The event applies to one security.
    MARKET
        The event applies to an entire market.
    VENUE
        The event applies to an exchange or trading venue.
    """

    SYMBOL = "symbol"
    MARKET = "market"
    VENUE = "venue"


@dataclass(frozen=True, slots=True, kw_only=True)
class MarketEvent:
    """
    Represent a market event delivered to a strategy.

    Attributes
    ----------
    type
        Type of market event.
    occurred_at
        Time when the market event occurred.
    """

    type: MarketEventType
    occurred_at: Timestamp


