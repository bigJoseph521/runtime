@dataclass(frozen=True, slots=True, kw_only=True)
class SplitEvent(MarketEvent):
    """
    Represent a stock split or reverse stock split.

    The runtime applies the split to positions, eligible open orders,
    account state, and affected indicator state before delivering the
    event to the strategy.

    Attributes
    ----------
    symbol
        Security affected by the split.
    new_shares
        Number of new shares in the split ratio.
    old_shares
        Number of old shares in the split ratio.
    effective_at
        Time when the split becomes effective.

    Examples
    --------
    A 2-for-1 split uses ``new_shares=2.0`` and ``old_shares=1.0``.

    A 1-for-10 reverse split uses ``new_shares=1.0`` and
    ``old_shares=10.0``.
    """

    type: MarketEventType = field(
        default=MarketEventType.SPLIT,
        init=False,
    )

    symbol: Symbol
    new_shares: float
    old_shares: float
    effective_at: Timestamp

    @property
    def quantity_factor(self) -> float:
        """
        Return the position-quantity adjustment factor.

        Returns
        -------
        float
            Number of new shares received for each old share.
        """
        return self.new_shares / self.old_shares

    @property
    def price_factor(self) -> float:
        """
        Return the corresponding price-adjustment factor.

        Returns
        -------
        float
            Factor applied to pre-split prices.
        """
        return self.old_shares / self.new_shares


