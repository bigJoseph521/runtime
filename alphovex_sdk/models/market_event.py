    """
    Represent a cash-dividend distribution.

    The runtime updates the account cash balance before delivering the
    event when the dividend payment becomes effective.

    Attributes
    ----------
    symbol
        Security paying the dividend.
    amount_per_share
        Dividend amount paid for each eligible share.
    currency
        Currency of the dividend payment.
    ex_date
        First trading date on which buyers do not receive the dividend.
    record_date
        Date on which eligible shareholders are recorded.
    payment_date
        Date on which the dividend is paid.
    """

    type: MarketEventType = field(
        default=MarketEventType.DIVIDEND,
        init=False,
    )

    symbol: Symbol
    amount_per_share: CashValue
    currency: Currency
    ex_date: date
    record_date: date | None = None
    payment_date: date | None = None


